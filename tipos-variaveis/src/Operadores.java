public class Operadores {
    
    public static void main(String[] args) {

        int numero1 = 2;
        int numero2 = 4;

        Boolean sinNao = numero1 == numero2;

        System.out.println("numeroUm é igual a numeroDois? " + sinNao);

        sinNao = numero1 != numero2;

        System.out.println("numeroUm é diferente a numeroDois? " + sinNao);


    }
}
